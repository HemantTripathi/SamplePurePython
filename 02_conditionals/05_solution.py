# Suggest an activity base don weather(e.g. Sunny - Go for a walk, Rainy-Read a book, Snowy-Build a snowman)

weather_type = "Rainy"

if weather_type == "Sunny":
    print("It is sunny, Go for a walk")
elif weather_type == "Rainy":
    print("It is rainy, Read a book")
elif weather_type == "Snowy":
    print("It is snowy, Build a snowman")
else:
    print("Weather information is not available")

